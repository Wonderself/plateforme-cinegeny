/* ── Helpers ── */

export const SHOWS_BY_GENRE: Record<string, TvShowData[]> = ALL_TV_SHOWS.reduce(
  (acc, show) => {
    if (!acc[show.genre]) acc[show.genre] = []
    acc[show.genre].push(show)
    return acc
  },
  {} as Record<string, TvShowData[]>,
)

export const SHOWS_BY_SLUG: Record<string, TvShowData> = ALL_TV_SHOWS.reduce(
  (acc, show) => {
    acc[show.slug] = show
    return acc
  },
  {} as Record<string, TvShowData>,
)

/** Get shows for a specific genre */
export function getShowsByGenre(genre: string): TvShowData[] {
  return SHOWS_BY_GENRE[genre] ?? []
}

/** Get a single show by slug */
export function getShowBySlug(slug: string): TvShowData | undefined {
  return SHOWS_BY_SLUG[slug]
}
