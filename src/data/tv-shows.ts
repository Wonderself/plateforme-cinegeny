/* ── Types ── */

export interface TvShowData {
  id: string
  title: string
  slug: string
  genre: string
  host: string
  cast: string[]
  synopsis: string
  episodeCount: number
  duration: number            // minutes per episode
  seasons: number
  episodesPerSeason: number
  episodeDuration: string     // human-readable e.g. "45min"
  year: number
  rating: number              // out of 5 (numeric)
  viewCount: number
  coverImageUrl: string | null
  tags: string[]
  premiered: string           // ISO date
  status: 'ongoing' | 'completed' | 'upcoming'
  viewersPct: number
  fundingPct: number
}

export interface TvEpisode {
  id: string
  showId: string
  title: string
  episode: number
  season: number
  airedAt: string
  duration: number
  viewCount: number
  thumbnailUrl: string | null
}

